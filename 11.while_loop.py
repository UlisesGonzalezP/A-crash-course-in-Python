#WHILE LOOP
#Este ejemplo imprime el contador mientras que sea menor a 5
count = 1
while count < 5 :
    print(count)
    count = count + 1
    
"""Lo siguiente es un bucle abierto, es decir, siempre se ejecuta y sirve para involucrar otra condición
while(true):
se puede detener con ctrl+c o involucrando otra condición."""
