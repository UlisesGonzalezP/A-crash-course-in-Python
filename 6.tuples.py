#TUPLES
#Los valores de una tupla no se pueden cambiar, se declara con ()
colors = ("red","yellow","blue")
print(colors)
colors2 = colors[0:2]#Notación de lista []
print(colors2)
#Una tupla puede ser remplazada
colors2 = (1,2,3)
print(colors2)
#También puede ser remplazada con una tupla vacia
colors2 = ()
print(colors2)